import ComponentCard from "@/components/component-card";
import { Container } from "@/components/container";

import { getAllPublicChats } from "./actions";

export const metadata = {
  title: `Public components - Tailwind AI`,
  description: "Last Tailwind components generated with AI by our users",
};

export default async function Featured() {
  const chats = await getAllPublicChats();

  return (
    <Container>
      <h1 className="mb-1 text-lg font-medium text-gray-900 sm:text-left sm:text-2xl">
        Public Components
      </h1>
      <h2 className="mb-8 text-lg text-gray-700 sm:text-left sm:text-xl">
        Last components generated by our users
      </h2>
      <div className="grid grid-cols-1 gap-3 pb-20 md:grid-cols-2 lg:grid-cols-3 2xl:grid-cols-4">
        {chats?.map((chat) => <ComponentCard key={chat.chat_id} chat={chat} />)}
      </div>
    </Container>
  );
}
